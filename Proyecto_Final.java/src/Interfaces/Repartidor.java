    private void registrarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarBotonActionPerformed
        String nombre = nombreRepartidortxt.getText();
        int edad = Integer.parseInt(edadRepartidortxt.getText());
        String minutos = tiempoLlegadatxt.getText();
        String vehiculo = transporteRepartidortxt.getText();
        int score = Integer.parseInt(calificaciontxt.getText());
        
        repartidorM = new ModeloRepartidor(vehiculo, edad, minutos , nombre, score);
        repartidorC.agregarRepartidor(repartidorM);
        
        this.dispose();
        new Pedido(repartidorC).setVisible(true);
        JOptionPane.showMessageDialog(this, "Ha sido registrado el repartidor "+nombreRepartidortxt.getText());  
    }//GEN-LAST:event_registrarBotonActionPerformed

